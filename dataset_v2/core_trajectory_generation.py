def run_core_trajectory_generation(
    dataset_root,
    master_seed: Optional[int] = None,
    overwrite: bool = False,
    anchor_ids: Optional[Sequence[str]] = None,
    shapes: Optional[Sequence[str]] = None,
    orientation_modes: Optional[Sequence[str]] = None,
    source_waypoint_count: Optional[int] = None,
    model_context: Optional[ModelContext] = None,
    dry_run: bool = False,
    progress: bool = False,
) -> CoreTrajectoryGenerationResult:
    """Generate Dataset v2's core trajectories: 5 shapes x 2 orientation modes x 12 anchors = 120
    (full/locked mode). ``anchor_ids``/``shapes``/``orientation_modes``/``source_waypoint_count``
    exist for tests/smoke runs only -- passing any of them marks the run as not
    ``full_locked_counts`` and the canonical waypoint count (400) never changes regardless.
    """
    paths = require_dataset_v2_root(dataset_root)
    trajectories_dir = paths.trajectories_dir

    full_locked_counts = anchor_ids is None and shapes is None and orientation_modes is None and source_waypoint_count is None

    anchor_npz_path = paths.anchors_dir / ANCHOR_NPZ_NAME
    if not anchor_npz_path.is_file():
        raise RuntimeError(
            f"anchor catalog not found at {anchor_npz_path}; run pipelines.run_dataset_v2_anchor_generation "
            "before core trajectory generation (Phase 5 depends on Phase 4's anchor catalog)."
        )

    anchor_report = validate_anchors(paths.root, full_counts=full_locked_counts)
    if not anchor_report.passed:
        raise RuntimeError(
            "anchor catalog failed independent validation; core trajectory generation refuses to "
            f"build on an invalid anchor catalog. Reasons: {anchor_report.reasons}"
        )

    anchor_rows = _load_anchor_rows(paths)
    if anchor_ids is not None:
        wanted = set(anchor_ids)
        anchor_rows = [row for row in anchor_rows if row["anchor_id"] in wanted]
        missing = wanted - {row["anchor_id"] for row in anchor_rows}
        if missing:
            raise ValueError(f"requested anchor_ids not found in anchor catalog: {sorted(missing)}")

    shape_list = list(shapes) if shapes is not None else list(CORE_SHAPES)
    orientation_list = list(orientation_modes) if orientation_modes is not None else list(ORIENTATION_MODES)
    for shape in shape_list:
        if shape not in CORE_SHAPES:
            raise ValueError(f"unknown shape '{shape}'; must be one of {CORE_SHAPES}")
    for mode in orientation_list:
        if mode not in ORIENTATION_MODES:
            raise ValueError(f"unknown orientation_mode '{mode}'; must be one of {ORIENTATION_MODES}")

    triples = [(row, shape, mode) for row in anchor_rows for shape in shape_list for mode in orientation_list]
    expected_ids = [f"core_{shape}_{mode}_{row['anchor_id']}" for row, shape, mode in triples]

    output_paths = [
        trajectories_dir / MANIFEST_NAME,
        trajectories_dir / REPORT_NAME,
        trajectories_dir / REACHABILITY_REPORT_NAME,
        trajectories_dir / ANTI_LEAKAGE_REPORT_NAME,
        trajectories_dir / GEOMETRY_SEARCH_REPORT_NAME,
    ]
    for (row, _shape, _mode), trajectory_id in zip(triples, expected_ids):
        split_dir = _split_dir(paths, row["split"])
        output_paths.append(split_dir / f"{trajectory_id}.npz")
        output_paths.append(split_dir / f"{trajectory_id}_source.npz")

    existing = [p for p in output_paths if p.is_file()]
    if existing and not overwrite:
        existing_relative = ", ".join(str(p.relative_to(paths.root)) for p in existing[:10])
        more = "" if len(existing) <= 10 else f" (+{len(existing) - 10} more)"
        raise FileExistsError(
            f"Core trajectory v2 output already exists ({existing_relative}{more}); pass overwrite=True "
            "(--overwrite on the CLI) to regenerate it."
        )

    seed_policy = load_json_config(paths.configs_dir / "seed_policy.json")
    resolved_master_seed = int(master_seed if master_seed is not None else seed_policy["master_seed"])
    core_component_tag = int(seed_policy["component_tags"]["core_trajectories"])
    core_component_seed = derive_seed(resolved_master_seed, core_component_tag)

    settings = load_core_trajectory_generation_settings(paths, source_waypoint_count)
    reach_settings = load_generation_reachability_settings(paths)
    probe = probe_settings_from(paths, reach_settings)

    if dry_run:
        return CoreTrajectoryGenerationResult(
            dataset_root=paths.root,
            trajectories_dir=trajectories_dir,
            dry_run=True,
            total_trajectories=len(triples),
            full_locked_counts=full_locked_counts,
            split_counts={s: sum(1 for row, _, _ in triples if row["split"] == s) for s in SPLITS},
            shape_counts={s: sum(1 for _, sh, _ in triples if sh == s) for s in CORE_SHAPES},
            orientation_counts={m: sum(1 for _, _, om in triples if om == m) for m in ORIENTATION_MODES},
        )

    model_context = model_context if model_context is not None else load_model_context()
    model_fingerprint = sha256_file(V1_MODEL_PATH)
    config_fingerprint = _config_fingerprint(paths)

    time_s, tau, s, _s_dot, _s_ddot, canonical_control_period_nominal_s = build_time_and_path_parameter(
        settings.source_waypoint_count, settings.duration_s
    )

    manifest_rows: List[list] = []
    trajectory_ids: List[str] = []
    content_hashes: List[str] = []
    reachability_records: List[dict] = []
    geometry_search_records: List[dict] = []
    failures: List[dict] = []
    per_shape_counts: Dict[str, int] = {shape: 0 for shape in CORE_SHAPES}
    per_orientation_counts: Dict[str, int] = {mode: 0 for mode in ORIENTATION_MODES}
    per_split_counts: Dict[str, int] = {name: 0 for name in SPLITS}
    per_anchor_counts: Dict[str, int] = {row["anchor_id"]: 0 for row in anchor_rows}

    ff_cfg = settings.geometry["free_form"]

    def _free_form_offsets(anchor_id: str, seed_offset: int, mirror: bool, split_name: str) -> dict:
        tags = [FREE_FORM_TAG, zlib.crc32(anchor_id.encode("utf-8")), seed_offset]
        if split_name == "frozen_test":
            tags.append(settings.frozen_core_seed_revision)
        seed = derive_seed(core_component_seed, *tags)
        return _free_form_unit_offsets(np.random.default_rng(seed), int(ff_cfg["control_point_count"]), mirror=mirror)

    import sys as _sys
    import time as _time

    total_triples = len(triples)
    run_started = _time.perf_counter()

    for triple_index, (row, shape, orientation_mode) in enumerate(triples, start=1):
        trajectory_started = _time.perf_counter()
        trajectory_id = f"core_{shape}_{orientation_mode}_{row['anchor_id']}"
        q_anchor = row["q"]
        fk_anchor = forward_kinematics(model_context, q_anchor)
        closed_path = SHAPE_CLOSED_PATH[shape]
        alternatives = enumerate_geometry_alternatives(shape, settings.geometry_alternatives)
        # frozen_test trajectories draw from a separate seed namespace revision (spec section K
        # frozen-test protocol): the Phase 5/5.1 frozen content was observed while generator policy
        # was still being tuned, so it is burned. development/validation keep their existing
        # namespace.
        if row["split"] == "frozen_test":
            path_seed = derive_seed(
                core_component_seed, PATH_SEED_TAG, zlib.crc32(trajectory_id.encode("utf-8")), settings.frozen_core_seed_revision
            )
        else:
            path_seed = derive_seed(core_component_seed, PATH_SEED_TAG, zlib.crc32(trajectory_id.encode("utf-8")))

        def build_fn(scale, alternative, shape=shape, fk_anchor=fk_anchor, orientation_mode=orientation_mode, closed_path=closed_path, row=row):
            ff_offsets = (
                _free_form_offsets(row["anchor_id"], alternative["seed_offset"], alternative["mirror"], row["split"])
                if shape == "free_form"
                else None
            )
            source_positions, geometry_params = build_source_positions(
                shape, fk_anchor, scale, s, settings.geometry, alternative, ff_offsets
            )
            # The variable-orientation rotation follows the path's traversal sense so the
            # orientation profile stays consistent with the geometry alternative (spec section 4).
            rotation_vector = (
                settings.orientation_rotation_angle_rad
                * scale
                * float(alternative.get("orientation_sign", 1.0))
                * settings.orientation_rotation_axis
                if orientation_mode == "variable"
                else None
            )
            source_quaternions = orientation_arrays(
                orientation_mode, fk_anchor.rotation_matrix, rotation_vector, s, closed_path, settings.source_waypoint_count
            )
            canonical = resample_canonical(time_s, tau, source_positions, source_quaternions, settings.canonical_waypoint_count)
            return {
                "source": {"time_s": time_s, "tau": tau, "target_position": source_positions, "target_quaternion": source_quaternions},
                "canonical": canonical,
                "geometry_params": geometry_params,
            }

        search = search_geometry_and_scale(
            model_context, probe, q_anchor, alternatives, build_fn, settings, path_seed, trajectory_id
        )
        geometry_search_records.append(
            {
                "trajectory_id": trajectory_id,
                "anchor_id": row["anchor_id"],
                "anchor_class": row["anchor_class"],
                "shape": shape,
                "orientation_mode": orientation_mode,
                "alternatives_available": [a["alternative_id"] for a in alternatives],
                "alternatives_evaluated": search.get("alternatives_evaluated", len(alternatives)),
                "attempts": search["attempts"],
                "accepted": search["accepted"],
                "accepted_alternative_id": None if not search["accepted"] else search["alternative"]["alternative_id"],
                "accepted_alternative_family": None if not search["accepted"] else search["alternative"].get("family"),
                "accepted_alternative_metadata": (
                    None
                    if not search["accepted"]
                    else {k: v for k, v in search["alternative"].items() if k not in ("alternative_id", "family")}
                ),
                "accepted_scale": search["scale"],
                "max_reachable_scale_observed": (
                    max([a["scale"] for a in search["attempts"] if a["reachable"]], default=None)
                ),
                "default_alternative_id": alternatives[0]["alternative_id"],
                "improved_over_default_alternative": (
                    None
                    if not search["accepted"]
                    else search["alternative"]["alternative_id"] != alternatives[0]["alternative_id"]
                ),
            }
        )

        if not search["accepted"]:
            if progress:
                print(
                    f"[core-trajectory-generate] {triple_index}/{total_triples} {trajectory_id} "
                    f"FAILED after {len(search['attempts'])} (alternative, scale) attempts "
                    f"[{_time.perf_counter() - trajectory_started:.1f}s]",
                    flush=True,
                    file=_sys.stderr,
                )
            failures.append(
                {
                    "trajectory_id": trajectory_id,
                    "anchor_id": row["anchor_id"],
                    "anchor_class": row["anchor_class"],
                    "shape": shape,
                    "orientation_mode": orientation_mode,
                    "reason": (
                        "no (geometry alternative, scale) candidate achieved strict reachability at or "
                        f"above the locked minimum accepted scale {search['effective_min_scale']:.4f} "
                        f"across {len(alternatives)} geometry alternative(s)"
                    ),
                    "effective_min_scale": search["effective_min_scale"],
                    "alternatives_available": [a["alternative_id"] for a in alternatives],
                    "attempts": search["attempts"],
                }
            )
            continue

        # Final acceptance: re-validate the winner end-to-end under the FULL (not probe) policy,
        # on the canonical path AND the high-resolution source path.
        built = build_fn(search["scale"], search["alternative"])
        canonical = built["canonical"]
        source = built["source"]

        canonical_result = validate_path_strict(
            model_context, reach_settings, q_anchor, canonical["target_position"], canonical["target_quaternion"], path_seed=path_seed
        )
        source_result = validate_path_strict(
            model_context, reach_settings, q_anchor, source["target_position"], source["target_quaternion"], path_seed=path_seed
        )

        if not canonical_result["all_reachable"] or not source_result["all_reachable"]:
            failures.append(
                {
                    "trajectory_id": trajectory_id,
                    "anchor_id": row["anchor_id"],
                    "anchor_class": row["anchor_class"],
                    "shape": shape,
                    "orientation_mode": orientation_mode,
                    "accepted_alternative_id": search["alternative"]["alternative_id"],
                    "accepted_scale": search["scale"],
                    "reason": "full-policy re-validation failed after the search accepted this candidate",
                    "canonical_first_failure_index": int(canonical_result["first_failure_index"]),
                    "source_first_failure_index": int(source_result["first_failure_index"]),
                }
            )
            continue

        canonical_errors = error_distribution(canonical_result["position_errors_m"], canonical_result["orientation_errors_rad"])
        source_errors = error_distribution(source_result["position_errors_m"], source_result["orientation_errors_rad"])

        arc_length_m = float(canonical["cumulative_arc_length_m"][-1])
        angular_displacement_rad = float(canonical["cumulative_angular_displacement_rad"][-1])
        closure_position_error_m = float(np.linalg.norm(canonical["target_position"][-1] - canonical["target_position"][0]))
        closure_orientation_error_rad = float(quaternion_geodesic_angle(canonical["target_quaternion"][0], canonical["target_quaternion"][-1]))
        band = scale_band_of(search["scale"], settings.scale_diagnostic_bands)

        content_hash = content_hash_of_record(
            {
                "anchor_content_hash": row["content_hash"],
                "shape": shape,
                "orientation_mode": orientation_mode,
                "geometry_alternative_id": search["alternative"]["alternative_id"],
                "accepted_scale": round(float(search["scale"]), 12),
                "geometry_parameters": built["geometry_params"],
                "target_position": [[round(float(v), 9) for v in p] for p in canonical["target_position"]],
                "target_quaternion": [[round(float(v), 9) for v in q] for q in canonical["target_quaternion"]],
                "model_fingerprint": model_fingerprint,
                "config_fingerprint": config_fingerprint,
            }
        )

        split_dir = _split_dir(paths, row["split"])
        canonical_npz_path = split_dir / f"{trajectory_id}.npz"
        source_npz_path = split_dir / f"{trajectory_id}_source.npz"

        canonical_arrays = {
            "waypoint_id": np.arange(settings.canonical_waypoint_count, dtype=np.int64),
            "time_s": canonical["time_s"].astype(np.float64),
            "source_parameter_u": canonical["source_parameter_u"].astype(np.float64),
            "cumulative_arc_length_m": canonical["cumulative_arc_length_m"].astype(np.float64),
            "target_position": canonical["target_position"].astype(np.float64),
            "target_quaternion": canonical["target_quaternion"].astype(np.float64),
            "cumulative_angular_displacement_rad": canonical["cumulative_angular_displacement_rad"].astype(np.float64),
            "q_reference": canonical_result["q_reference"].astype(np.float64),
            "position_reconstruction_error_m": canonical_result["position_errors_m"].astype(np.float64),
            "orientation_reconstruction_error_rad": canonical_result["orientation_errors_rad"].astype(np.float64),
            "waypoint_reachable": canonical_result["reachable"].astype(bool),
        }
        source_arrays = {
            "waypoint_id": np.arange(settings.source_waypoint_count, dtype=np.int64),
            "time_s": source["time_s"].astype(np.float64),
            "tau": source["tau"].astype(np.float64),
            "target_position": source["target_position"].astype(np.float64),
            "target_quaternion": source["target_quaternion"].astype(np.float64),
            "q_reference": source_result["q_reference"].astype(np.float64),
            "position_reconstruction_error_m": source_result["position_errors_m"].astype(np.float64),
            "orientation_reconstruction_error_rad": source_result["orientation_errors_rad"].astype(np.float64),
            "waypoint_reachable": source_result["reachable"].astype(bool),
        }
        if shape == "free_form":
            source_arrays["control_points_m"] = np.array(built["geometry_params"]["control_points_m"], dtype=np.float64)

        save_npz(canonical_npz_path, canonical_arrays, overwrite=overwrite)
        save_npz(source_npz_path, source_arrays, overwrite=overwrite)

        sha = sha256_file(canonical_npz_path)
        source_sha = sha256_file(source_npz_path)

        manifest_rows.append(
            [
                trajectory_id,
                "core",
                row["split"],
                shape,
                orientation_mode,
                row["anchor_id"],
                row["anchor_class"],
                row["content_hash"],
                core_component_seed,
                settings.source_waypoint_count,
                settings.canonical_waypoint_count,
                "wxyz",
                f"{settings.duration_s:.10f}",
                f"{canonical_control_period_nominal_s:.10f}",
                "1.0",
                f"{search['scale']:.10f}",
                band,
                search["alternative"]["alternative_id"],
                search["alternative"].get("family", ""),
                json.dumps(
                    {k: v for k, v in search["alternative"].items() if k not in ("alternative_id", "family")},
                    sort_keys=True,
                ),
                search["alternatives_evaluated"],
                len(search["attempts"]),
                search["scale_reduction_reason"] or "",
                json.dumps(built["geometry_params"], sort_keys=True),
                f"{arc_length_m:.10f}",
                f"{angular_displacement_rad:.10f}",
                bool(closed_path),
                f"{closure_position_error_m:.10f}",
                f"{closure_orientation_error_rad:.10f}",
                "validated",
                f"{reach_settings.position_tolerance_m:.10g}",
                f"{reach_settings.orientation_tolerance_deg:.10g}",
                f"{canonical_errors['position_max_m']:.12g}",
                f"{canonical_errors['orientation_max_deg']:.12g}",
                f"{source_errors['position_max_m']:.12g}",
                f"{source_errors['orientation_max_deg']:.12g}",
                int(np.sum(canonical_result["reachable"])),
                int(np.sum(source_result["reachable"])),
                "development",
                model_fingerprint,
                config_fingerprint,
                content_hash,
                sha,
                source_sha,
            ]
        )
        trajectory_ids.append(trajectory_id)
        content_hashes.append(content_hash)
        reachability_records.append(
            {
                "trajectory_id": trajectory_id,
                "anchor_class": row["anchor_class"],
                "shape": shape,
                "orientation_mode": orientation_mode,
                "accepted_scale": search["scale"],
                "scale_band": band,
                "geometry_alternative_id": search["alternative"]["alternative_id"],
                "canonical": canonical_errors,
                "source": source_errors,
                "canonical_restarts_used": int(np.sum(canonical_result["restarts_used"])),
                "source_restarts_used": int(np.sum(source_result["restarts_used"])),
                "canonical_waypoints_reachable": int(np.sum(canonical_result["reachable"])),
                "source_waypoints_reachable": int(np.sum(source_result["reachable"])),
            }
        )
        per_shape_counts[shape] += 1
        per_orientation_counts[orientation_mode] += 1
        per_split_counts[row["split"]] += 1
        per_anchor_counts[row["anchor_id"]] += 1

        if progress:
            elapsed = _time.perf_counter() - trajectory_started
            total_elapsed = _time.perf_counter() - run_started
            print(
                f"[core-trajectory-generate] {triple_index}/{total_triples} {trajectory_id} "
                f"alt={search['alternative']['alternative_id']} scale={search['scale']:.4f} "
                f"probes={len(search['attempts'])} canon_pos_max={canonical_errors['position_max_m']:.2e}m "
                f"src_pos_max={source_errors['position_max_m']:.2e}m [{elapsed:.1f}s, total {total_elapsed / 60:.1f}min]",
                flush=True,
                file=_sys.stderr,
            )

    if failures:
        raise RuntimeError(
            f"{len(failures)} of {len(triples)} core trajectories failed strict reachability generation; "
            "refusing to write a partial core trajectory set (locked count is "
            f"{len(triples)}). Failures: {json.dumps(failures[:5], indent=2)}"
            + ("" if len(failures) <= 5 else f"\n... and {len(failures) - 5} more")
        )

    if len(set(trajectory_ids)) != len(trajectory_ids):
        raise ValueError("duplicate trajectory_id found across the generated core trajectory set")
    if len(set(content_hashes)) != len(content_hashes):
        raise ValueError("duplicate content_hash found across the generated core trajectory set")

    ids_by_split: Dict[str, List[str]] = {name: [] for name in SPLITS}
    hashes_by_split: Dict[str, List[str]] = {name: [] for name in SPLITS}
    for (row, shape, mode), trajectory_id, content_hash in zip(triples, trajectory_ids, content_hashes):
        ids_by_split[row["split"]].append(trajectory_id)
        hashes_by_split[row["split"]].append(content_hash)

    collisions = []
    split_names = list(SPLITS)
    for i in range(len(split_names)):
        for j in range(i + 1, len(split_names)):
            a, b = split_names[i], split_names[j]
            id_overlap = set(ids_by_split[a]) & set(ids_by_split[b])
            hash_overlap = set(hashes_by_split[a]) & set(hashes_by_split[b])
            if id_overlap or hash_overlap:
                collisions.append({"splits": [a, b], "trajectory_id_overlap": sorted(id_overlap), "content_hash_overlap": sorted(hash_overlap)})

    anti_leakage_report = {
        "dimensions_checked": ["trajectory_id", "trajectory_content_hash", "anchor_id"],
        "collisions_found": len(collisions),
        "collision_details": collisions,
        "pass": len(collisions) == 0,
    }
    if collisions:
        raise ValueError(f"cross-split leakage detected in core trajectory generation: {collisions}")

    manifest_path = _atomic_write_csv(trajectories_dir / MANIFEST_NAME, MANIFEST_COLUMNS, manifest_rows)
    _atomic_write_json(trajectories_dir / ANTI_LEAKAGE_REPORT_NAME, anti_leakage_report)
    _atomic_write_json(trajectories_dir / GEOMETRY_SEARCH_REPORT_NAME, {"trajectories": geometry_search_records})

    scale_values = [rec["accepted_scale"] for rec in reachability_records]
    band_counts: Dict[str, int] = {}
    for rec in reachability_records:
        band_counts[rec["scale_band"]] = band_counts.get(rec["scale_band"], 0) + 1
    below_counts = {
        "below_0.75": sum(1 for v in scale_values if v < 0.75),
        "below_0.50": sum(1 for v in scale_values if v < 0.50),
        "below_0.25": sum(1 for v in scale_values if v < 0.25),
    }
    scale_statistics = {
        "min_accepted_scale": min(scale_values),
        "p05_accepted_scale": float(np.percentile(scale_values, 5)),
        "median_accepted_scale": float(np.median(scale_values)),
        "mean_accepted_scale": float(np.mean(scale_values)),
        "max_accepted_scale": max(scale_values),
        "trajectories_requiring_shrink": sum(1 for v in scale_values if v < 1.0),
        "band_counts": band_counts,
        **below_counts,
    }

    gate_violations = []
    if settings.minimum_scale_enforced and settings.minimum_core_accepted_scale is not None:
        gate_violations = [rec["trajectory_id"] for rec in reachability_records if rec["accepted_scale"] < settings.minimum_core_accepted_scale]
        if gate_violations:
            raise RuntimeError(
                f"{len(gate_violations)} trajectories fall below the locked "
                f"minimum_core_accepted_scale={settings.minimum_core_accepted_scale}: {gate_violations}"
            )

    worst = sorted(reachability_records, key=lambda r: -max(r["canonical"]["position_max_m"], r["source"]["position_max_m"]))[:10]
    per_shape_errors = {}
    for shape in CORE_SHAPES:
        subset = [r for r in reachability_records if r["shape"] == shape]
        if subset:
            per_shape_errors[shape] = {
                "canonical_position_max_m": max(r["canonical"]["position_max_m"] for r in subset),
                "canonical_orientation_max_deg": max(r["canonical"]["orientation_max_deg"] for r in subset),
                "source_position_max_m": max(r["source"]["position_max_m"] for r in subset),
                "source_orientation_max_deg": max(r["source"]["orientation_max_deg"] for r in subset),
            }

    reachability_statistics = {
        "tolerance_position_m": reach_settings.position_tolerance_m,
        "tolerance_orientation_deg": reach_settings.orientation_tolerance_deg,
        "tolerance_source": "configs/generation_reachability_config.json (Dataset v2's own; never the DLS baseline evaluation config)",
        "all_canonical_waypoints_reachable": all(r["canonical_waypoints_reachable"] == settings.canonical_waypoint_count for r in reachability_records),
        "all_source_waypoints_reachable": all(r["source_waypoints_reachable"] == settings.source_waypoint_count for r in reachability_records),
        "canonical_position_max_m": max(r["canonical"]["position_max_m"] for r in reachability_records),
        "canonical_position_p95_m": float(np.percentile([r["canonical"]["position_p95_m"] for r in reachability_records], 95)),
        "canonical_orientation_max_deg": max(r["canonical"]["orientation_max_deg"] for r in reachability_records),
        "canonical_orientation_p95_deg": float(np.percentile([r["canonical"]["orientation_p95_deg"] for r in reachability_records], 95)),
        "source_position_max_m": max(r["source"]["position_max_m"] for r in reachability_records),
        "source_position_p95_m": float(np.percentile([r["source"]["position_p95_m"] for r in reachability_records], 95)),
        "source_orientation_max_deg": max(r["source"]["orientation_max_deg"] for r in reachability_records),
        "source_orientation_p95_deg": float(np.percentile([r["source"]["orientation_p95_deg"] for r in reachability_records], 95)),
        "per_shape_error_distribution": per_shape_errors,
        "worst_10_trajectories": [
            {
                "trajectory_id": r["trajectory_id"],
                "canonical_position_max_m": r["canonical"]["position_max_m"],
                "source_position_max_m": r["source"]["position_max_m"],
                "accepted_scale": r["accepted_scale"],
            }
            for r in worst
        ],
        "total_restarts_used_diagnostic": sum(r["canonical_restarts_used"] + r["source_restarts_used"] for r in reachability_records),
    }
    _atomic_write_json(
        trajectories_dir / REACHABILITY_REPORT_NAME,
        {"statistics": reachability_statistics, "trajectories": reachability_records},
    )

    alternatives_attempted_total = sum(len(rec["attempts"]) for rec in geometry_search_records)
    non_default_alternative_count = sum(
        1 for rec in geometry_search_records if rec["accepted"] and rec["accepted_alternative_id"] != rec["alternatives_available"][0]
    )

    generated_at = datetime.now(timezone.utc).isoformat()
    git_commit = _git_commit(REPO_ROOT)
    report = {
        "dataset_version": DATASET_VERSION,
        "schema_version": DATASET_SCHEMA_VERSION,
        "generator_version": GENERATOR_VERSION,
        "git_commit": git_commit,
        "model_fingerprint": model_fingerprint,
        "config_fingerprint": config_fingerprint,
        "master_seed": resolved_master_seed,
        "generation_timestamp_utc": generated_at,
        "generation_status": "development",
        "seed_derivation": {
            "core_trajectories_component_tag": core_component_tag,
            "core_trajectories_component_seed": core_component_seed,
            "frozen_core_seed_revision": settings.frozen_core_seed_revision,
            "frozen_seed_policy": (
                "frozen_test trajectory path seeds and free-form templates mix in "
                "frozen_core_seed_revision; development/validation use the unrevised namespace"
            ),
        },
        "full_locked_counts": full_locked_counts,
        "source_waypoint_count": settings.source_waypoint_count,
        "canonical_waypoint_count": settings.canonical_waypoint_count,
        "duration_s": settings.duration_s,
        "total_trajectories": len(trajectory_ids),
        "shape_counts": per_shape_counts,
        "orientation_counts": per_orientation_counts,
        "split_counts": per_split_counts,
        "anchor_counts": per_anchor_counts,
        "canonical_poses_total": len(trajectory_ids) * settings.canonical_waypoint_count,
        "scale_statistics": scale_statistics,
        "minimum_scale_gate": {
            "minimum_core_accepted_scale": settings.minimum_core_accepted_scale,
            "enforced": settings.minimum_scale_enforced,
            "violations": gate_violations,
        },
        "geometry_search": {
            "total_attempts": alternatives_attempted_total,
            "trajectories_using_non_default_alternative": non_default_alternative_count,
        },
        "reachability_statistics": reachability_statistics,
        "anti_leakage_report": anti_leakage_report,
        "output_files": {
            "manifest": {"filename": relative_to_dataset_v2_root(manifest_path, paths.root), "sha256": sha256_file(manifest_path)},
        },
    }
    _atomic_write_json(trajectories_dir / REPORT_NAME, report)

    manifest = json.loads(paths.manifest_file.read_text(encoding="utf-8"))
    manifest = apply_core_trajectory_generation_status(
        manifest,
        total_trajectories=len(trajectory_ids),
        split_counts=per_split_counts,
        shape_counts=per_shape_counts,
        orientation_counts=per_orientation_counts,
        canonical_waypoints_per_trajectory=settings.canonical_waypoint_count,
    )
    _atomic_write_json(paths.manifest_file, manifest)

    checksum_manifest = build_checksum_manifest(paths.root)
    _atomic_write_json(paths.checksum_manifest_file, checksum_manifest)

    return CoreTrajectoryGenerationResult(
        dataset_root=paths.root,
        trajectories_dir=trajectories_dir,
        dry_run=False,
        total_trajectories=len(trajectory_ids),
        full_locked_counts=full_locked_counts,
        split_counts=per_split_counts,
        shape_counts=per_shape_counts,
        orientation_counts=per_orientation_counts,
        scale_statistics=scale_statistics,
        reachability_statistics=reachability_statistics,
        report=report,
    )
