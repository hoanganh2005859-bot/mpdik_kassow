"""Dataset v2 core trajectory generator: 120 trajectories = 5 shapes x 2 orientation modes x
12 anchors (Phase 5, hardened in Phase 5.1).

Every trajectory has a high-resolution source representation and a canonical representation of
exactly 400 waypoints, arc-length-resampled from the source (never independently regenerated by a
different formula). Position geometry for line/circle/figure8/helix reuses the closed-form
parametric definitions already proven in v1's ``generators/generate_*_trajectory.py`` (unchanged
math); free_form is a deterministic seeded cubic spline through a handful of control points.
Orientation profiles reuse ``generators/generate_orientation_profile.py`` unchanged (SO(3)
geodesic SLERP for ``variable``, constant for ``fixed``).

**Phase 5.1 acceptance rule**: a waypoint counts as reachable only when a reference configuration
exists whose *independently recomputed* FK reproduces the target pose within Dataset v2's own
strict tolerances (``configs/generation_reachability_config.json``) -- never because the numerical
IK engine reported success, and never against a Dataset v1 DLS *baseline evaluation* threshold.
Geometry-basis alternatives are searched before any scale reduction, so shrinking is a last
resort rather than the first response to a hard anchor.

This module never modifies Dataset v1, never touches FK/Jacobian/pose-error/DLS formulas, and
never uses global ``numpy.random`` state.
"""

