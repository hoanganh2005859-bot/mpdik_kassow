def _load_anchor_rows(paths: DatasetV2Paths) -> List[dict]:
    npz_path = paths.anchors_dir / ANCHOR_NPZ_NAME
    if not npz_path.is_file():
        raise RuntimeError(
            f"anchor catalog not found at {npz_path}; run pipelines.run_dataset_v2_anchor_generation "
            "before core trajectory generation (Phase 5 depends on Phase 4's anchor catalog)."
        )
    arrays = load_npz(npz_path)
    n = arrays["anchor_id"].shape[0]
    rows = []
    for i in range(n):
        rows.append(
            {
                "anchor_id": str(arrays["anchor_id"][i]),
                "split": str(arrays["split"][i]),
                "anchor_class": ANCHOR_CLASS_ID_TO_NAME[int(arrays["anchor_class_id"][i])],
                "q": arrays["q"][i],
                "position": arrays["position"][i],
                "quaternion_wxyz": arrays["quaternion_wxyz"][i],
                "content_hash": str(arrays["content_hash"][i]),
            }
        )
    rows.sort(key=lambda r: r["anchor_id"])
    return rows


def _atomic_write_json(path: Path, payload: dict) -> Path:
    path.parent.mkdir(parents=True, exist_ok=True)
    tmp_path = path.with_name(path.name + ".tmp.json")
    tmp_path.write_text(json.dumps(payload, indent=2, sort_keys=False) + "\n", encoding="utf-8")
    tmp_path.replace(path)
    return path


def _atomic_write_csv(path: Path, header: List[str], rows: List[list]) -> Path:
    path.parent.mkdir(parents=True, exist_ok=True)
    tmp_path = path.with_name(path.name + ".tmp.csv")
    with open(tmp_path, "w", newline="", encoding="utf-8") as handle:
        writer = csv.writer(handle)
        writer.writerow(header)
        writer.writerows(rows)
    tmp_path.replace(path)
    return path


def _git_commit(repo_root: Path) -> Optional[str]:
    import subprocess

    try:
        result = subprocess.run(["git", "-C", str(repo_root), "rev-parse", "HEAD"], capture_output=True, text=True, timeout=30)
    except (OSError, subprocess.SubprocessError):
        return None
    if result.returncode != 0:
        return None
    return result.stdout.strip()


def _config_fingerprint(paths: DatasetV2Paths) -> str:
    import hashlib

    trajectory_config = load_json_config(paths.configs_dir / "trajectory_config.json")
    reachability_config = load_json_config(paths.configs_dir / "generation_reachability_config.json")
    payload = {"trajectory_config": trajectory_config, "generation_reachability_config": reachability_config}
    encoded = json.dumps(payload, sort_keys=True, separators=(",", ":")).encode("utf-8")
    return hashlib.sha256(encoded).hexdigest()


