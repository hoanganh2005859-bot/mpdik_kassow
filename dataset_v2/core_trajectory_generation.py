def _axis_vector(fk_anchor, name: str) -> np.ndarray:
    """Anchor end-effector local axis (world frame) named '+x'/'-x'/'x'/'y'/'z'/..."""
    sign = -1.0 if name.startswith("-") else 1.0
    axis = name[-1]
    column = {"x": 0, "y": 1, "z": 2}[axis]
    return sign * fk_anchor.rotation_matrix[:, column]


def enumerate_geometry_alternatives(shape: str, alternatives_cfg: dict) -> List[dict]:
    """Deterministic, config-driven list of geometry alternatives to try for ``shape``.

    Every alternative carries a canonical ``alternative_id`` plus the structured metadata the
    manifest/report and validator need (plane basis, traversal direction, handedness, phase,
    axis swap, template id). Enumeration order is fixed and the ids are lexically comparable, so
    the final tie-break is deterministic. Phase 5.3 completes the geometric symmetry that Phase
    5.1 only gave to ``line`` and ``helix``.
    """
    cfg = alternatives_cfg[shape]
    alternatives: List[dict] = []

    if shape == "line":
        for axis in cfg["direction_axes"]:
            alternatives.append(
                {
                    "alternative_id": f"line_dir{axis}",
                    "family": "line_direction",
                    "direction_axis": axis,
                    "orientation_sign": 1.0,
                }
            )
    elif shape == "circle":
        for u_axis, v_axis in cfg["plane_bases"]:
            for traversal in cfg["traversal_directions"]:
                sigma = 1.0 if traversal == "ccw" else -1.0
                for phase in cfg["start_phases_rad"]:
                    phase_label = f"ph{int(round(np.degrees(phase))):03d}"
                    alternatives.append(
                        {
                            "alternative_id": f"circle_{u_axis}{v_axis}_{traversal}_{phase_label}",
                            "family": f"circle_{traversal}",
                            "plane_basis_id": f"{u_axis}{v_axis}",
                            "u_axis": u_axis,
                            "v_axis": v_axis,
                            "traversal_direction": traversal,
                            "traversal_sign": sigma,
                            "start_phase_rad": float(phase),
                            "phase_id": phase_label,
                            # orientation follows the traversal sense (spec section 4)
                            "orientation_sign": sigma,
                        }
                    )
    elif shape == "figure8":
        for u_axis, v_axis in cfg["plane_bases"]:
            for sign_a, sign_b in cfg["amplitude_signs"]:
                for axis_swap in cfg["axis_swaps"]:
                    a_label = "p" if sign_a > 0 else "n"
                    b_label = "p" if sign_b > 0 else "n"
                    swap_label = "swap" if axis_swap else "noswap"
                    # (sign_a, sign_b) == (-1, -1) is exactly the s -> 1-s traversal reversal.
                    traversal = "reversed" if (sign_a < 0 and sign_b < 0) else "forward"
                    handedness = "left" if sign_b < 0 else "right"
                    alternatives.append(
                        {
                            "alternative_id": f"figure8_{u_axis}{v_axis}_a{a_label}b{b_label}_{swap_label}",
                            "family": f"figure8_{handedness}_{traversal}",
                            "plane_basis_id": f"{u_axis}{v_axis}",
                            "u_axis": u_axis,
                            "v_axis": v_axis,
                            "sign_a": float(sign_a),
                            "sign_b": float(sign_b),
                            "handedness": handedness,
                            "traversal_direction": traversal,
                            "axis_swap": bool(axis_swap),
                            "orientation_sign": float(sign_a),
                        }
                    )
    elif shape == "helix":
        for u_axis, v_axis, w_axis in cfg["axis_bases"]:
            for height_sign in cfg["height_signs"]:
                sign_label = "p" if height_sign > 0 else "n"
                alternatives.append(
                    {
                        "alternative_id": f"helix_{u_axis}{v_axis}{w_axis}_{sign_label}",
                        "family": f"helix_{sign_label}",
                        "u_axis": u_axis,
                        "v_axis": v_axis,
                        "w_axis": w_axis,
                        "height_sign": float(height_sign),
                        "orientation_sign": 1.0,
                    }
                )
    elif shape == "free_form":
        for template in cfg["templates"]:
            alternatives.append(
                {
                    "alternative_id": f"free_form_{template['template_id']}",
                    "family": "free_form_mirrored" if template["mirror"] else "free_form_direct",
                    "template_id": str(template["template_id"]),
                    "departure_axis": str(template["departure_axis"]),
                    "mirror": bool(template["mirror"]),
                    "seed_offset": int(template["seed_offset"]),
                    "orientation_sign": 1.0,
                }
            )
    else:
        raise ValueError(f"unknown shape '{shape}'")
    return alternatives


# ---------------------------------------------------------------------------------------------
# Free-form control points (deterministic, seeded per anchor + template)
# ---------------------------------------------------------------------------------------------


def _free_form_unit_offsets(rng: np.random.Generator, control_point_count: int, mirror: bool = False) -> dict:
    """Seeded unit-scale control-point offsets for one free-form template.

    ``mirror`` reflects the lateral offsets, producing a genuine mirrored path rather than a new
    random draw, so the mirrored template stays a controlled variant of its parent.
    """
    tilt = np.array([rng.uniform(-0.4, 0.4), rng.uniform(-0.4, 0.4)])
    n_interior = control_point_count - 2
    lateral_multipliers = rng.uniform(-1.0, 1.0, size=(n_interior, 2))
    if mirror:
        lateral_multipliers = lateral_multipliers * np.array([-1.0, 1.0])
        tilt = tilt * np.array([-1.0, 1.0])
    return {"tilt": tilt, "lateral_multipliers": lateral_multipliers, "mirror": bool(mirror)}


def _free_form_control_points(
    fk_anchor,
    unit_offsets: dict,
    scale: float,
    endpoint_distance_m: float,
    lateral_amplitude_m: float,
    departure_axis: str,
) -> np.ndarray:
    """Control points for one free-form template: anchor position, seeded interior offsets, and a
    seeded endpoint displaced along the template's departure axis.

    The departure axis is one of the anchor's six signed local axes, giving horizontal / vertical /
    mixed workspace variants across the template set; the two orthogonal local axes carry the
    lateral offsets. The anchor pose is always the first control point.
    """
    p0 = fk_anchor.position
    primary = _axis_vector(fk_anchor, departure_axis)
    # deterministic orthogonal complement of the departure axis, taken from the anchor frame
    axis_index = {"x": 0, "y": 1, "z": 2}[departure_axis[-1]]
    lateral_1 = fk_anchor.rotation_matrix[:, (axis_index + 1) % 3]
    lateral_2 = fk_anchor.rotation_matrix[:, (axis_index + 2) % 3]

    tilt = unit_offsets["tilt"]
    direction = primary + tilt[0] * lateral_1 + tilt[1] * lateral_2
    direction = direction / np.linalg.norm(direction)

    endpoint_distance = endpoint_distance_m * scale
    p_end = p0 + endpoint_distance * direction

    lateral_multipliers = unit_offsets["lateral_multipliers"]
    lateral_amplitude = lateral_amplitude_m * scale
    n_interior = lateral_multipliers.shape[0]

    points = [p0]
    for i in range(n_interior):
        t = (i + 1) / (n_interior + 1)
        base = p0 + t * (p_end - p0)
        lateral = lateral_amplitude * (lateral_multipliers[i, 0] * lateral_1 + lateral_multipliers[i, 1] * lateral_2)
        points.append(base + lateral)
    points.append(p_end)
    return np.array(points, dtype=np.float64)


def _free_form_positions(control_points: np.ndarray, s: np.ndarray) -> np.ndarray:
    diffs = np.diff(control_points, axis=0)
    seg_lengths = np.linalg.norm(diffs, axis=1)
    cum = np.concatenate([[0.0], np.cumsum(seg_lengths)])
    total = cum[-1]
    t_control = np.linspace(0.0, 1.0, control_points.shape[0]) if total < 1e-9 else cum / total

    spline_x = CubicSpline(t_control, control_points[:, 0])
    spline_y = CubicSpline(t_control, control_points[:, 1])
    spline_z = CubicSpline(t_control, control_points[:, 2])
    return np.stack([spline_x(s), spline_y(s), spline_z(s)], axis=1)


# ---------------------------------------------------------------------------------------------
# Shape position builders (closed-form for line/circle/figure8/helix, reused unchanged from v1's
# generators/generate_*_trajectory.py math; free_form is new)
# ---------------------------------------------------------------------------------------------


def build_source_positions(
    shape: str, fk_anchor, scale: float, s: np.ndarray, geometry_cfg: dict, alternative: dict, free_form_unit_offsets: Optional[dict] = None
) -> Tuple[np.ndarray, dict]:
    p0 = fk_anchor.position

    if shape == "line":
        u = _axis_vector(fk_anchor, alternative["direction_axis"])
        length = float(geometry_cfg["line"]["nominal_length_m"]) * scale
        p1 = p0 + length * u
        positions = p0[None, :] + s[:, None] * (p1 - p0)[None, :]
        params = {"length_m": length, "direction_axis": alternative["direction_axis"], "direction_vector": u.tolist()}
    elif shape == "circle":
        u = _axis_vector(fk_anchor, alternative["u_axis"])
        v = _axis_vector(fk_anchor, alternative["v_axis"])
        sigma = float(alternative["traversal_sign"])
        phi = float(alternative["start_phase_rad"])
        r = float(geometry_cfg["circle"]["nominal_radius_m"]) * scale
        # centre chosen so p(0) == anchor position for every (traversal, phase) combination
        center = p0 - r * (np.cos(phi) * u + sigma * np.sin(phi) * v)
        angle = TWO_PI * s + phi
        positions = center[None, :] + r * np.cos(angle)[:, None] * u[None, :] + sigma * r * np.sin(angle)[:, None] * v[None, :]
        params = {
            "radius_m": r,
            "center_m": center.tolist(),
            "plane_basis": [alternative["u_axis"], alternative["v_axis"]],
            "plane_u": u.tolist(),
            "plane_v": v.tolist(),
            "traversal_direction": alternative["traversal_direction"],
            "start_phase_rad": phi,
        }
    elif shape == "figure8":
        u = _axis_vector(fk_anchor, alternative["u_axis"])
        v = _axis_vector(fk_anchor, alternative["v_axis"])
        nominal_a = float(geometry_cfg["figure8"]["nominal_amplitude_a_m"])
        nominal_b = float(geometry_cfg["figure8"]["nominal_amplitude_b_m"])
        # axis_swap exchanges which basis axis carries the major amplitude; the nominal
        # amplitudes themselves are unchanged.
        e1, e2 = (v, u) if alternative["axis_swap"] else (u, v)
        a = nominal_a * scale * float(alternative["sign_a"])
        b = nominal_b * scale * float(alternative["sign_b"])
        center = p0
        positions = center[None, :] + a * np.sin(TWO_PI * s)[:, None] * e1[None, :] + b * np.sin(FOUR_PI * s)[:, None] * e2[None, :]
        params = {
            "amplitude_a_m": nominal_a * scale,
            "amplitude_b_m": nominal_b * scale,
            "center_m": center.tolist(),
            "plane_basis": [alternative["u_axis"], alternative["v_axis"]],
            "major_axis": e1.tolist(),
            "minor_axis": e2.tolist(),
            "sign_a": float(alternative["sign_a"]),
            "sign_b": float(alternative["sign_b"]),
            "handedness": alternative["handedness"],
            "traversal_direction": alternative["traversal_direction"],
            "axis_swap": bool(alternative["axis_swap"]),
        }
    elif shape == "helix":
        u = _axis_vector(fk_anchor, alternative["u_axis"])
        v = _axis_vector(fk_anchor, alternative["v_axis"])
        w = _axis_vector(fk_anchor, alternative["w_axis"]) * float(alternative["height_sign"])
        r = float(geometry_cfg["helix"]["nominal_radius_m"]) * scale
        h = float(geometry_cfg["helix"]["nominal_height_m"]) * scale
        center = p0 - r * u + 0.5 * h * w
        angle = TWO_PI * s
        positions = (
            center[None, :]
            + r * np.cos(angle)[:, None] * u[None, :]
            + r * np.sin(angle)[:, None] * v[None, :]
            + h * (s[:, None] - 0.5) * w[None, :]
        )
        params = {
            "radius_m": r,
            "height_m": h,
            "center_m": center.tolist(),
            "axis_basis": [alternative["u_axis"], alternative["v_axis"], alternative["w_axis"]],
            "height_sign": float(alternative["height_sign"]),
            "axis_w": w.tolist(),
        }
    elif shape == "free_form":
        ff_cfg = geometry_cfg["free_form"]
        if free_form_unit_offsets is None:
            raise ValueError("free_form requires free_form_unit_offsets")
        control_points = _free_form_control_points(
            fk_anchor,
            free_form_unit_offsets,
            scale,
            float(ff_cfg["endpoint_distance_m"]),
            float(ff_cfg["lateral_amplitude_m"]),
            alternative["departure_axis"],
        )
        positions = _free_form_positions(control_points, s)
        params = {
            "control_points_m": control_points.tolist(),
            "template_id": alternative["template_id"],
            "departure_axis": alternative["departure_axis"],
            "mirror": bool(alternative["mirror"]),
        }
    else:
        raise ValueError(f"unknown shape '{shape}'")

    return positions, params


# ---------------------------------------------------------------------------------------------
# Canonical (arc-length) resampling with SO(3) SLERP orientation interpolation
# ---------------------------------------------------------------------------------------------


def _slerp_quaternion_wxyz(q0: np.ndarray, q1: np.ndarray, t: float) -> np.ndarray:
    if t <= 0.0:
        return canonicalize_quaternion_wxyz(q0)
    if t >= 1.0:
        return canonicalize_quaternion_wxyz(q1)
    R0 = quaternion_wxyz_to_matrix(q0)
    R1 = quaternion_wxyz_to_matrix(q1)
    relative_log = so3_log(R0.T @ R1)
    R_t = R0 @ so3_exp(t * relative_log)
    return rotation_matrix_to_quaternion_wxyz(R_t)


def _canonicalize_quat_sequence(quaternions: np.ndarray) -> np.ndarray:
    out = np.empty_like(quaternions)
    out[0] = canonicalize_quaternion_wxyz(quaternions[0])
    for i in range(1, quaternions.shape[0]):
        q = canonicalize_quaternion_wxyz(quaternions[i])
        if np.dot(q, out[i - 1]) < 0.0:
            q = -q
        out[i] = q
    return out


def resample_canonical(
    source_time_s: np.ndarray,
    source_tau: np.ndarray,
    source_positions: np.ndarray,
    source_quaternions: np.ndarray,
    canonical_count: int,
) -> dict:
    """Arc-length-uniform resample of a high-resolution source path to ``canonical_count``
    waypoints. Position is linearly interpolated between the two bracketing source samples;
    orientation uses SO(3) geodesic SLERP (never linear-interpolate-then-normalize). Endpoints are
    preserved exactly. Never re-evaluates the shape's geometry formula.
    """
    n_source = source_positions.shape[0]
    seg_lengths = np.linalg.norm(np.diff(source_positions, axis=0), axis=1)
    source_arc_length = np.concatenate([[0.0], np.cumsum(seg_lengths)])
    total_arc_length = float(source_arc_length[-1])

    target_arc_lengths = np.zeros(canonical_count) if total_arc_length < 1e-12 else np.linspace(0.0, total_arc_length, canonical_count)

    idx_hi = np.searchsorted(source_arc_length, target_arc_lengths, side="left")
    idx_hi = np.clip(idx_hi, 1, n_source - 1)
    idx_lo = idx_hi - 1

    seg_span = source_arc_length[idx_hi] - source_arc_length[idx_lo]
    safe_span = np.where(seg_span > 1e-12, seg_span, 1.0)
    frac = np.clip((target_arc_lengths - source_arc_length[idx_lo]) / safe_span, 0.0, 1.0)

    # Exact endpoint preservation: arc length 0 -> source[0], arc length total -> source[-1].
    frac[0] = 0.0
    idx_lo[0], idx_hi[0] = 0, min(1, n_source - 1)
    frac[-1] = 1.0
    idx_lo[-1], idx_hi[-1] = n_source - 2, n_source - 1

    canonical_positions = source_positions[idx_lo] + frac[:, None] * (source_positions[idx_hi] - source_positions[idx_lo])
    canonical_time_s = source_time_s[idx_lo] + frac * (source_time_s[idx_hi] - source_time_s[idx_lo])
    canonical_tau = source_tau[idx_lo] + frac * (source_tau[idx_hi] - source_tau[idx_lo])

    canonical_quaternions_raw = np.empty((canonical_count, 4))
    for i in range(canonical_count):
        canonical_quaternions_raw[i] = _slerp_quaternion_wxyz(source_quaternions[idx_lo[i]], source_quaternions[idx_hi[i]], float(frac[i]))
    canonical_quaternions = _canonicalize_quat_sequence(canonical_quaternions_raw)

    cumulative_arc_length = np.concatenate([[0.0], np.cumsum(np.linalg.norm(np.diff(canonical_positions, axis=0), axis=1))])
    cumulative_angular = np.zeros(canonical_count)
    for i in range(1, canonical_count):
        cumulative_angular[i] = cumulative_angular[i - 1] + quaternion_geodesic_angle(canonical_quaternions[i - 1], canonical_quaternions[i])

    return {
        "time_s": canonical_time_s,
        "source_parameter_u": canonical_tau,
        "target_position": canonical_positions,
        "target_quaternion": canonical_quaternions,
        "cumulative_arc_length_m": cumulative_arc_length,
        "cumulative_angular_displacement_rad": cumulative_angular,
    }


def scale_band_of(scale: float, bands: Sequence[float]) -> str:
    """Diagnostic band label for an accepted scale (bands are descending upper bounds)."""
    ordered = sorted(bands, reverse=True)
    for i, upper in enumerate(ordered):
        if scale >= upper:
            return f">={upper:g}"
        if i + 1 < len(ordered) and scale >= ordered[i + 1]:
            return f"[{ordered[i + 1]:g},{upper:g})"
    return f"<{ordered[-1]:g}"


# ---------------------------------------------------------------------------------------------
# Geometry-alternative + scale search (scale outer, alternatives inner -> shrink is last resort)
# ---------------------------------------------------------------------------------------------


def search_geometry_and_scale(
    model_context: ModelContext,
    probe_settings,
    q_anchor: np.ndarray,
    alternatives: List[dict],
    build_fn,
    settings: CoreTrajectoryGenerationSettings,
    path_seed: int,
    label: str,
) -> dict:
    """Find the largest scale at which *some* geometry alternative is strictly reachable.

    Scale is the outer loop and geometry alternative the inner loop, so every alternative is tried
    at the larger scale before any shrinking happens -- scale reduction is genuinely a last resort.
    Among alternatives tying at the same scale, the earliest in the configured order wins
    (deterministic). Probing uses the weaker probe settings purely for speed; the winner is
    re-validated under the full policy by the caller.
    """
    # The locked minimum-scale gate is a hard floor on the search ladder, not a post-hoc filter:
    # a trajectory that cannot reach the gate fails generation outright rather than being accepted
    # below it and rejected later.
    effective_min_scale = settings.min_scale
    if settings.minimum_scale_enforced and settings.minimum_core_accepted_scale is not None:
        effective_min_scale = max(settings.min_scale, settings.minimum_core_accepted_scale)

    scale_schedule: List[float] = []
    scale = 1.0
    for _ in range(settings.max_shrink_attempts):
        if scale < effective_min_scale:
            break
        scale_schedule.append(scale)
        scale *= settings.shrink_factor

    attempts: List[dict] = []
    best: Optional[dict] = None

    def _beats(candidate: dict, incumbent: Optional[dict]) -> bool:
        """Locked tie-break order: larger scale, then smaller strict position error, then fewer
        refinement/restart attempts (diagnostic only), then lexical alternative_id. Never uses DLS
        runtime, iteration count, or any baseline evaluation outcome."""
        if incumbent is None:
            return True
        key_new = (-candidate["scale"], candidate["position_error_max_m"], candidate["restarts_used"], candidate["alternative_id"])
        key_old = (-incumbent["scale"], incumbent["position_error_max_m"], incumbent["restarts_used"], incumbent["alternative_id"])
        return key_new < key_old

    for alternative in alternatives:
        for candidate_scale in scale_schedule:
            # An alternative can no longer win once its remaining schedule is strictly below the
            # best scale found so far. Ties are still evaluated so the tie-break can see them.
            if best is not None and candidate_scale < best["scale"]:
                break

            built = build_fn(candidate_scale, alternative)
            canonical = built["canonical"]
            result = validate_path_strict(
                model_context,
                probe_settings,
                q_anchor,
                canonical["target_position"],
                canonical["target_quaternion"],
                path_seed=path_seed,
                stop_on_first_failure=True,
            )
            reachable = bool(result["all_reachable"])
            attempts.append(
                {
                    "scale": candidate_scale,
                    "alternative_id": alternative["alternative_id"],
                    "family": alternative.get("family"),
                    "reachable": reachable,
                    "first_failure_index": int(result["first_failure_index"]),
                    "waypoints_checked": int(result["waypoints_checked"]),
                    "rejection_reason": (
                        None
                        if reachable
                        else (
                            f"waypoint {int(result['first_failure_index'])} has no reference solution within "
                            f"{probe_settings.position_tolerance_m} m / {probe_settings.orientation_tolerance_deg} deg"
                        )
                    ),
                }
            )
            if reachable:
                candidate = {
                    "alternative": alternative,
                    "alternative_id": alternative["alternative_id"],
                    "scale": candidate_scale,
                    "position_error_max_m": float(np.max(result["position_errors_m"])),
                    "restarts_used": int(np.sum(result["restarts_used"])),
                }
                if _beats(candidate, best):
                    best = candidate
                break  # this alternative's largest reachable scale is known

    if best is None:
        return {
            "accepted": False,
            "alternative": None,
            "scale": None,
            "attempts": attempts,
            "scale_reduction_reason": None,
            "effective_min_scale": effective_min_scale,
            "alternatives_evaluated": len(alternatives),
        }

    return {
        "accepted": True,
        "alternative": best["alternative"],
        "scale": best["scale"],
        "attempts": attempts,
        "alternatives_evaluated": len(alternatives),
        "effective_min_scale": effective_min_scale,
        "scale_reduction_reason": (
            "none (nominal scale)"
            if best["scale"] >= 1.0
            else (
                f"no geometry alternative among {len(alternatives)} was strictly reachable above scale "
                f"{best['scale']:.6f}; {sum(1 for a in attempts if not a['reachable'])} (alternative, scale) "
                "attempt(s) rejected"
            )
        ),
    }


